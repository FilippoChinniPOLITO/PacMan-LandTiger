void toggle_timer( uint8_t timer_num ) {
	if ( timer_num == 0 )
	{
		LPC_TIM0->TCR = !LPC_TIM0->TCR;
	}
	else if (timer_num == 1)
	{
		LPC_TIM1->TCR = !LPC_TIM1->TCR;
	}
	else if (timer_num == 2)
	{
		LPC_TIM2->TCR = !LPC_TIM2->TCR;
	}
	else //if (timer_num == 3)
	{
		LPC_TIM3->TCR = !LPC_TIM3->TCR;
	}
	return;
}

